    private void btnExportarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExportarActionPerformed
        // TODO add your handling code here:
        javax.swing.JFileChooser selector = new javax.swing.JFileChooser();
        selector.setDialogTitle("Guardar inventario como...");
        selector.setSelectedFile(new java.io.File("inventario.csv"));

        int opcion = selector.showSaveDialog(this);

        if (opcion == javax.swing.JFileChooser.APPROVE_OPTION) {
        String ruta = selector.getSelectedFile().getAbsolutePath();
    
        if (!ruta.toLowerCase().endsWith(".csv")) {
        ruta = ruta + ".csv";
        }
    
        try {
        util.ArchivoUtil.exportarCSV(productoNegocio.listarProductos(), ruta);
        JOptionPane.showMessageDialog(this, "Inventario exportado correctamente a:\n" + ruta);
        } catch (excepciones.ArchivoException ex) {
        JOptionPane.showMessageDialog(this, ex.getMessage(), "Error al exportar", JOptionPane.ERROR_MESSAGE);
        }
    }
    }//GEN-LAST:event_btnExportarActionPerformed

