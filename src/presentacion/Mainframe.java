    private void actualizarTabla(java.util.List<Producto> lista) {
    javax.swing.table.DefaultTableModel modelo = (javax.swing.table.DefaultTableModel) tblProductos.getModel();
    modelo.setRowCount(0);
    
    for (Producto p : lista) {
        Object[] fila = {
            p.getId(),
            p.getCodigo(),
            p.getNombre(),
            p.getCategoria(),
            p.getCantidad(),
            p.getPrecio(),
            p.isDisponible()
        };
        modelo.addRow(fila);
    
    }   
    }
